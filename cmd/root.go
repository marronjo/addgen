package cmd

import (
	"github.com/spf13/cobra"
	"os"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "addgen",
	Short: "CLI for generating crypto addresses",
	Long: `A powerful CLI used to generate new/existing crypto addresses:

It allows for addresses to be generated from an existing private key or
new key generation for supported address types.`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

